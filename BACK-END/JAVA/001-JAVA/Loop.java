class Loop{
    public static void main(String[]args){
        //for

        for(int i = 0; i<10;i++){
            System.out.println(i);
        }


        //while
        int contador = 1;
        while(contador <=10){
            System.out.println(contador);
            contador ++;
        }

    contador = 20;
    do {    
        System.out.println(contador);
    } while (contador <=10);

    }
}